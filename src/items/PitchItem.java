package items;

/**
 * 
 * @author Arno
 *
 */
public class PitchItem {

	private String name;
	private String path;
	
	public PitchItem(String name, String path) {
		this.name = name;
		this.path = path;
	}
	
	public String getName() {
		return name;
	}
	
	public String getPath() {
		return path;
	}
	
}
